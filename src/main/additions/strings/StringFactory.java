package main.additions.strings;

public class StringFactory {
	
	public static void main(String[] args) {
		String temporalString = "Helok";
		System.out.println(temporalString.substring(0, 2));
		System.out.println(temporalString.substring(3));
		System.out.println(" :) - (: ".trim());
		System.out.println(temporalString.intern());
		System.out.println(temporalString.concat(" 1155"));
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println();

		
	}
}
